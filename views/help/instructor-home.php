<h4>General Help</H4>
<p>Use this page to add questions for your students to answer. Once you add a question it is immediately
    available to students.</p>
<h5>Adding a Question</h5>
<ol>
    <li>Click "Add Question"</li>
    <li>Enter the question text.</li>
    <li>Click the save icon or press "Enter" on your keyboard</li>
</ol>
<h5>Modifying Questions</h5>
<p>Use the icons to the right of an added question to edit the text, move it up in the list, or delete it
    along with any answers.</p>
<h5>Editing the Title</h5>
<p>You can edit the title of this Code Test by clicking the edit icon next to the title at the top of this
    page.</p>
<p>If there are no question in this Code Test, you can also edit its type.</p>
